num = int(input('Digite um numero de 1 a 10:'))

if (num == 1): print('Um')
elif (num == 2): print('Dois')
elif (num == 3): print('Três')
elif (num == 4): print('Quatro')
elif (num == 5): print('Cinco')
elif (num == 6): print('Seis')
elif (num == 7): print('Sete')
elif (num == 8): print('Oito')
elif (num == 9): print('Nove')
elif (num == 10): print('Dez')
else:
    print('Número inváldo')
